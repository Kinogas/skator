// プレイヤーを動かす
void player_move(int selected_map_grid[][X_SIZE], int *p_pos, int *step_num, int *is_clear, int line_gap, int row_gap, int is_walking, int *key_num, int *is_having_sord)
{
  if(selected_map_grid[p_pos[0] + line_gap][p_pos[1] + row_gap] == WALL && is_walking == 0) // 最初に向いた方向が壁
  {
    printf("そっちは壁です\n");
    (*step_num)--;
  }
  else if(selected_map_grid[p_pos[0] + line_gap][p_pos[1] + row_gap] == DOOR && *key_num == 0) // 鍵ドア（鍵なし）
  {
    printf("ドアにはカギ(\x1b[33mx-o\x1b[39m)がかかっています\n");
    if(is_walking == 0)
      (*step_num)--;
  }
  else if(MON1 <= selected_map_grid[p_pos[0] + line_gap][p_pos[1] + row_gap] &&
          selected_map_grid[p_pos[0] + line_gap][p_pos[1] + row_gap] <= MON3) // モンスターと戦闘
  {
    int is_winner;
    
    if(selected_map_grid[p_pos[0] + line_gap][p_pos[1] + row_gap] == MON1)
      is_winner = monster_battle(step_num ,selected_map_grid[p_pos[0] + line_gap][p_pos[1] + row_gap],*is_having_sord, 15, 1, 35, 5);
    if(selected_map_grid[p_pos[0] + line_gap][p_pos[1] + row_gap] == MON2)
      is_winner = monster_battle(step_num ,selected_map_grid[p_pos[0] + line_gap][p_pos[1] + row_gap],*is_having_sord, 20, 1, 50, 5);

    if(is_winner == 1)
    {
      line_gap = line_gap_change_and_return(line_gap, 1);
      row_gap = row_gap_change_and_return(row_gap, 1);

      selected_map_grid[p_pos[0] + line_gap][p_pos[1] + row_gap] = ROAD;
    }

    print_map(selected_map_grid, p_pos);
  }
  else if(selected_map_grid[p_pos[0] + line_gap][p_pos[1] + row_gap] != WALL)
  { 
    p_pos[0] += line_gap;
    p_pos[1] += row_gap;

    print_map(selected_map_grid, p_pos);

    if(selected_map_grid[p_pos[0]][p_pos[1]] == ROAD || // 道
       selected_map_grid[p_pos[0]][p_pos[1]] == STAR)
    {
      line_gap = line_gap_change_and_return(line_gap, 1);
      row_gap = row_gap_change_and_return(row_gap, 1);

      is_walking = 1;

      player_move(selected_map_grid, p_pos, step_num, is_clear, line_gap, row_gap, is_walking, key_num, is_having_sord);
    }
    else if(selected_map_grid[p_pos[0]][p_pos[1]] == JUMP) // ジャンプ
    {  
      line_gap = line_gap_change_and_return(line_gap, 2);
      row_gap = row_gap_change_and_return(row_gap, 2);

      player_move(selected_map_grid, p_pos, step_num, is_clear, line_gap, row_gap, is_walking, key_num, is_having_sord);
    }
    else if(selected_map_grid[p_pos[0]][p_pos[1]] == TLP1 || // テレポート
            selected_map_grid[p_pos[0]][p_pos[1]] == TLP2 ||
            selected_map_grid[p_pos[0]][p_pos[1]] == TLP3)
    {
      int teleport_entrance_pos[2] = {p_pos[0], p_pos[1]};

      int i,j;
      for(i = 0; i < Y_SIZE; i++)
      {
        for(j = 0; j < X_SIZE; j++)
        {
          if(selected_map_grid[i][j] == selected_map_grid[teleport_entrance_pos[0]][teleport_entrance_pos[1]] &&
             (teleport_entrance_pos[0] != i || teleport_entrance_pos[1] != j))
          {
            p_pos[0] = i;
            p_pos[1] = j;
          }
        }
      }
      print_map(selected_map_grid, p_pos);

      line_gap = line_gap_change_and_return(line_gap, 1);
      row_gap = row_gap_change_and_return(row_gap, 1);

      player_move(selected_map_grid, p_pos, step_num, is_clear, line_gap, row_gap, is_walking, key_num, is_having_sord);
    }
    else if(selected_map_grid[p_pos[0]][p_pos[1]] == _UP_) // ベルトコンベア上
    {
      line_gap = -1;
      row_gap = 0;

      player_move(selected_map_grid, p_pos, step_num, is_clear, line_gap, row_gap, is_walking, key_num, is_having_sord);
    }
    else if(selected_map_grid[p_pos[0]][p_pos[1]] == LEFT) // ベルトコンベア左
    {
      line_gap = 0;
      row_gap = -1;

      player_move(selected_map_grid, p_pos, step_num, is_clear, line_gap, row_gap, is_walking, key_num, is_having_sord);
    }
    else if(selected_map_grid[p_pos[0]][p_pos[1]] == DOWN) // ベルトコンベア下
    {
      line_gap = 1;
      row_gap = 0;

      player_move(selected_map_grid, p_pos, step_num, is_clear, line_gap, row_gap, is_walking, key_num, is_having_sord);
    }
    else if(selected_map_grid[p_pos[0]][p_pos[1]] == RIGH) // ベルトコンベア右
    {
      line_gap = 0;
      row_gap = 1;

      player_move(selected_map_grid, p_pos, step_num, is_clear, line_gap, row_gap, is_walking, key_num, is_having_sord);
    }
    else if(selected_map_grid[p_pos[0]][p_pos[1]] == KEY) // カギ
    {
      line_gap = line_gap_change_and_return(line_gap, 1);
      row_gap = row_gap_change_and_return(row_gap, 1);

      selected_map_grid[p_pos[0]][p_pos[1]] = ROAD;
      (*key_num)++;

      player_move(selected_map_grid, p_pos, step_num, is_clear, line_gap, row_gap, is_walking, key_num, is_having_sord);
    }
    else if(selected_map_grid[p_pos[0]][p_pos[1]] == DOOR) // カギドア（鍵あり）
    {
      line_gap = line_gap_change_and_return(line_gap, 1);
      row_gap = row_gap_change_and_return(row_gap, 1);

      selected_map_grid[p_pos[0]][p_pos[1]] = ROAD;
      (*key_num)--;

      player_move(selected_map_grid, p_pos, step_num, is_clear, line_gap, row_gap, is_walking, key_num, is_having_sord);
    }
    else if(selected_map_grid[p_pos[0]][p_pos[1]] == SORD) // 剣
    {
      line_gap = line_gap_change_and_return(line_gap, 1);
      row_gap = row_gap_change_and_return(row_gap, 1);

      selected_map_grid[p_pos[0]][p_pos[1]] = ROAD;
      (*is_having_sord)++;

      player_move(selected_map_grid, p_pos, step_num, is_clear, line_gap, row_gap, is_walking, key_num, is_having_sord);
    }
    else if(selected_map_grid[p_pos[0]][p_pos[1]] == GOAL) // ゴール
    {
      *is_clear = 1;
    }
  }

}

