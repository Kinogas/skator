// マップ番号をタイトルで表示する
void print_playable_map(int *map_clear_holder)
{
  printf(" ==== マップ番号 ====\n\n");

  int i; 
  for(i = 0; i < MAX_MAP_NUM; i++)
  {
    if(i < 9)
    {
      if(map_clear_holder[i] == 0)
        printf("   %d", i + 1);
      if(map_clear_holder[i] == 1)
        printf("   \x1b[36m%d\x1b[39m", i + 1);
      if(map_clear_holder[i] == 2)
        printf("   \x1b[33m%d\x1b[39m", i + 1);
    }
    if(i >= 9)
    {
      if(map_clear_holder[i] == 0)
        printf("  %d", i + 1);
      if(map_clear_holder[i] == 1)
        printf("  \x1b[36m%d\x1b[39m", i + 1);
      if(map_clear_holder[i] == 2)
        printf("  \x1b[33m%d\x1b[39m", i + 1);
    }
    if((i + 1) % 5 == 0 && (i + 1) != MAX_MAP_NUM )
      printf("\n\n");
  }

  printf("\n\n");

  printf(" ====================\n\n");
}

