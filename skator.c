// マップを表示する
void print_map(int map_grid[][X_SIZE], int *p_pos)
{
  int all_emp = 1;
  int i, j;

  printf("\n  ");

  for (i = 0; i < Y_SIZE; i++)
  {
    for (j = 0; j < X_SIZE; j++)
    {
      if (i == p_pos[0] && j == p_pos[1])
      {
        // \x1b[31mがここから先赤色、\x1b[39mがここで色変更終わり
        printf("\x1b[31m P \x1b[39m"); 
      }
      else
      {
        switch (map_grid[i][j])
        {
        case WALL:
          printf(" W ");
          all_emp = 1;
          break;
        case STAR:
        case ROAD:
          printf(" - ");
          all_emp = 1;
          break;
        case GOAL:
          printf("\x1b[32m G \x1b[39m");
          all_emp = 1;
          break;
        case JUMP:
          printf("\x1b[33m J \x1b[39m");
          all_emp = 1;
          break;
        case _UP_ :
          printf("\x1b[36m ^ \x1b[39m");
          all_emp = 1;
          break;
        case LEFT :
          printf("\x1b[36m < \x1b[39m");
          all_emp = 1;
          break;
        case DOWN :
          printf("\x1b[36m v \x1b[39m");
          all_emp = 1;
          break;
        case RIGH :
          printf("\x1b[36m > \x1b[39m");
          all_emp = 1;
          break;
        case EMPT:
          printf("   "); 
          break;
        case TLP1 :
          printf("\x1b[41m[1]\x1b[49m");
          all_emp = 1;
          break;
        case TLP2 :
          printf("\x1b[44m[2]\x1b[49m");
          all_emp = 1;
          break;
        case TLP3 :
          printf("\x1b[43m[3]\x1b[49m");
          all_emp = 1;
          break;
        case DOOR :
          printf("\x1b[33m\x1b[45m|x|\x1b[39m\x1b[49m");
          all_emp = 1;
          break;
        case KEY :
          printf("\x1b[33mx-o\x1b[39m");
          all_emp = 1;
          break;
        case SORD :
          printf("\x1b[33m--+\x1b[39m");
          all_emp = 1;
          break;
        case MON1 :
          printf("\x1b[42m:_:\x1b[49m");
          all_emp = 1;
          break;
        case MON2 :
          printf("\x1b[47m\x1b[31m:_:\x1b[49m\x1b[39m");
          all_emp = 1;
          break;
        default:
          break;
        }
      }
    }
    if (all_emp == 1)
      printf("\n  ");
    all_emp = 0;
  }
  printf("\n");
}

